package com.example.bitbybit;

public class NewsIntake {

    String Time;
    String Calories;
    String Carbohydrates;
    String Protein;
    String Fat;
    String Name;

    public NewsIntake(String Time, String Calories, String Carbohydrates, String Protein, String Fat, String Name){
        this.Time = Time;
        this.Calories = Calories;
        this.Carbohydrates = Carbohydrates;
        this.Protein = Protein;
        this.Fat = Fat;
        this.Name = Name;
    }

}
